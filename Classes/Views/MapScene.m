	if( (self=[super init] )) {
		CCTMXTiledMap* tileMap = [CCTMXTiledMap tiledMapWithTMXFile:@"map-1.tmx"];
		[self addChild:tileMap z:-1 tag:kMAP];
        [self schedule:@selector(nextFrame:)];
	}
	return self;
}

