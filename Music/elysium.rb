require 'rubygems'
require 'zgomot'

#.........................................................................................................
before_start do
  Zgomot.logger.level = Logger::DEBUG
end

#.........................................................................................................
# lead1
#.........................................................................................................
lead1_p1 = []
4.times do |i|
  lead1_p1 << np([:As,4], :mixolydian, :l=>16, :v=>0.99)[i+2,i+1,i+4,i+3]
end
lead1 = [lead1_p1]

#.........................................................................................................
# lead2
#.........................................................................................................
lead2_p1 = []
4.times do |i|
  lead2_p1 << np([:Gs,4], :dorian, :l=>16, :v=>0.99)[i+1,i+2,i+3,i+4].bpm!(10.0/11.0)
end
lead2 = [lead2_p1]

#.........................................................................................................
str 'lead1' do
  c = (count - 1) % lead1.length
  ch(0) << lead1[c]
end

#.........................................................................................................
str 'lead2' do
  c = (count - 1) % lead2.length
  ch(1) << lead2[c]
end

play